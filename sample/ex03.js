function plus(n1,n2){
    return n1+n2;
}

function minus(n1,n2){
    return n1-n2;
}

function multi(n1,n2){
    return n1*n2;
}

function div(n1,n2){
    return n1/n2;
}

var p = plus(8,4);
var m1 = minus(8,4);
var m2 = multi(8,4);
var d = div(8,4);

console.log(p);
console.log(m1);
console.log(m2);
console.log(d);